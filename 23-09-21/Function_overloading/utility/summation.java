package com.thbs.utility;

public class summation {
    public int add(int x,int y)
    {
        return (x+y);

    }
    public double add(double x,double y)
    {
        return(x+y);
    }

    public double add(int x,float y,double z)
    {
        return (x+y+z);
    }
    public String add(String s1,String s2)
    {
        return(s1+" "+s2);

    }

}
